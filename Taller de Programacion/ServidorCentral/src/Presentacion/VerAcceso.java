    private void resetTabla(JTable Tabla) {
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        modelo.setColumnIdentifiers(new Object[]{"Numero", "Usuario", "Ip", "URL", "Browser", "S.O."});

        IAcceso ca = Fabrica.getIA();

        for (DataAcceso dA : ca.listarAccesos()) {
            modelo.addRow(new Object[]{dA.getNumero(), dA.getUser(), dA.getIP(), dA.getURL(), dA.getBrowser(), dA.getSistOper()});
        }
        Tabla.setModel(modelo);

    }

