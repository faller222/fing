    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        try {
            // Click en cancelar orden
            if (selec == null) {
                MostrarMensaje("Seleccione una Orden de Compra", "Error");
            } else {
                int n = JOptionPane.showConfirmDialog(
                        this,
                        "¬øRealmente desea cancelar esta orden?", "Confirmar",
                        JOptionPane.YES_NO_OPTION);
                if (n == JOptionPane.YES_OPTION) {
                    IVCO.seleccionarOrden(selec.getNumero());
                    IVCO.cancelarOrden();
                    Datas = IVCO.listarOrdenesRecibidas();
                    resetTablaOrdenes(TablaO);
                    selec = null;
                    paneLinea.setVisible(false);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

