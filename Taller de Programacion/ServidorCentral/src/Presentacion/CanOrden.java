    private void resetTablaOrdenes(JTable TablaO) {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        modelo.setColumnIdentifiers(new Object[]{"Numero", "Fecha", "Costo Total(U$S)"});
        for (Map.Entry<Integer, DataOrdenCompra> entry : Datas.entrySet()) {
            DataOrdenCompra dC = entry.getValue();
            Date F = dC.getEstado().getFecha();
            modelo.addRow(new Object[]{dC.getNumero(), sdf.format(F), dC.getTotal()});
        }
        TablaO.setModel(modelo);
    }

    private void resetTablaLines(JTable TablaL) {

        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        modelo.setColumnIdentifiers(new Object[]{"Nombre producto", "Cantidad", "Precio unitario", "Subtotal"});
        for (Iterator it = Lines.iterator(); it.hasNext();) {
            DataLinea dl = (DataLinea) it.next();
            modelo.addRow(new Object[]{dl.getProd().getNombre(), dl.getCantidad(), dl.getPrecioProd(), dl.getPrecioLinea()});
        }
        TablaL.setModel(modelo);
        paneLinea.setVisible(true);
    }

