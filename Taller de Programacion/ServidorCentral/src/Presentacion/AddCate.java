    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if (IAC.buscarCategoria(txtNombre.getText()) != null) {
                MostrarMensaje("Ya existe esa Categoria", "Error");
            } else {
                if (boxPadre.isSelected()) {//Si tiene padre
                    if (Arbol.getLeadSelectionPath() == null) {
                        MostrarMensaje("Debe seleccionar una categoria", "Error");
                    } else {//si selecciono
                        String Padre = Arbol.getLeadSelectionPath().getLastPathComponent().toString();
                        if (IAC.buscarCategoria(Padre).getEsSimple()) {
                            MostrarMensaje("La categoria que selecciono esta dispuesta a contener Productos", "Error");
                        } else {//si es compuesta
                            IAC.altaCategoria(txtNombre.getText(), boxProds.isSelected(), Padre);
                            MostrarMensaje("La categoria se agrego correctamente", "Correcto");
                            doClose();
                        }
                    }
                } else {//Si no tiene padre
                    IAC.altaCategoria(txtNombre.getText(), boxProds.isSelected());
                    MostrarMensaje("La categoria se agrego correctamente", "Correcto");
                    doClose();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

