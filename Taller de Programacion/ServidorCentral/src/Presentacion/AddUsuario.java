    private void AddUsuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddUsuActionPerformed
        IAltaUsuario IAU = Fabrica.getIAU();

        boolean Correcto = true;

        //Chequeo Nickname Vacio
        if (Correcto & txtNick.getText().equals("")) {
            MostrarMensaje("Debe Ingresar un Nickname.", "Error");
            Correcto = false;
        }

        //Chequeo Password Vacio
        if (Correcto & txtPass.getText().equals("")) {
            MostrarMensaje("Debe Ingresar un PassWord.", "Error");
            Correcto = false;
        }

        //Chequeo Password Coincide
        if (Correcto & !txtPass.getText().equals(txtRPass.getText())) {
            MostrarMensaje("Las Contrase単as no coinciden.", "Error");
            Correcto = false;
        }


        //Chequeo Nombre Vacio
        if (Correcto & txtNombre.getText().equals("")) {
            MostrarMensaje("Debe Ingresar un Nombre.", "Error");
            Correcto = false;
        }

        //Chequeo Apellido Vacio
        if (Correcto & txtApellido.getText().equals("")) {
            MostrarMensaje("Debe Ingresar un Apellido.", "Error");
            Correcto = false;
        }

        //Chequeo el Mail
        if (Correcto & !isEmail(txtMail.getText())) {
            MostrarMensaje("Es Incorrecto el E-Mail.", "Error");
            Correcto = false;
        }

        //Chequeo la Fecha
        if (Correcto & chsrDate.getDate() == null) {
            MostrarMensaje("Problemas en la Fecha.", "Error");
            Correcto = false;
        }

        if (Correcto & chsrDate.getDate().after(new Date())) {
            MostrarMensaje("Fecha Posterior a hoy.", "Error");
            Correcto = false;
        }

        String imagePath = "";
        if (imageFile != null) {
            imagePath = imageFile.getPath();
        }
        DataUsuario Nuevo = null;
        try {
            if (rdProve.isSelected()) {

                //Chequeo Comapnia Vacio
                if (Correcto & txtComp.getText().equals("")) {
                    MostrarMensaje("Debe Ingresar una Coma単ia.", "Error");
                    Correcto = false;
                }

                //Chequeo WEB Vacio
                if (Correcto & txtWeb.getText().equals("")) {
                    MostrarMensaje("Debe Ingresar un Sitio Web.", "Error");
                    Correcto = false;
                }

                Nuevo = new DataProveedor(txtNick.getText(), txtPass.getText(), txtMail.getText(), txtNombre.getText(), txtApellido.getText(), chsrDate.getDate());
                Nuevo.setImagen(imagePath);
                ((DataProveedor) Nuevo).setCompania(txtComp.getText());
                ((DataProveedor) Nuevo).setSitioWeb(txtWeb.getText());
            } else {
                Nuevo = new DataCliente(txtNick.getText(), txtPass.getText(), txtMail.getText(), txtNombre.getText(), txtApellido.getText(), chsrDate.getDate());
                Nuevo.setImagen(imagePath);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        //chequeo las exeptions 
        if (Correcto) {
            try {
                IAU.ingresarDataUsuario(Nuevo);
                IAU.altaUsuario();
            } catch (Exception e) {
                Correcto = false;
                MostrarMensaje(e.getMessage(), "Error");
            }
        }

        if (Correcto) {
            MostrarMensaje("El Usuario se registro correctamente.", "Correcto");
            doClose();
        }
    }//GEN-LAST:event_AddUsuActionPerformed

