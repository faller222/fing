    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked

        if (Tabla.getSelectedRow() != -1) {
            DataProveedor selec = (DataProveedor) Datas.get(Tabla.getValueAt(Tabla.getSelectedRow(), 0).toString());
            txtNombre.setText(selec.getNombre());
            txtApellido.setText(selec.getApellido());
            Date F = selec.getFechaNac();

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            txtFecha.setText(sdf.format(F));

            txtNick.setText(selec.getNickname());
            txtMail.setText(selec.getMail());

            txtWeb.setText(selec.getSitioWeb());
            txtComp.setText(selec.getCompania());

            Etiqueta.setText("");
            try {
                if (selec.getBimagen() == null) {
                    Etiqueta.setIcon(null);
                } else {

                    Etiqueta.setIcon(Presentacion.fixImagen(UtilImage.toBufferedImage(selec.getBimagen()), Etiqueta));
                }
            } catch (Exception e) {
            }
            repaint();
            paneDatos.setVisible(true);
        } else {
            paneDatos.setVisible(false);
        }
    }//GEN-LAST:event_TablaMouseClicked

