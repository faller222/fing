    private void resetTabla(JTable Tabla) {
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        modelo.setColumnIdentifiers(new Object[]{"NickName", "E-Mail"});
        for (Map.Entry<String, DataCliente> entry : Datas.entrySet()) {
            modelo.addRow(new Object[]{entry.getValue().getNickname(), entry.getValue().getMail()});
        }
        Tabla.setModel(modelo);

    }

    private void resetTablaOrdenes(JTable TablaO) {
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

            DefaultTableModel modelo = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return false;
                }
            };
            modelo.setColumnIdentifiers(new Object[]{"Numero", "Modificada", "Costo Total(U$S)"});

            doc = IVC.listarOrdenesCliente();
            for (Map.Entry<Integer, DataOrdenCompra> entry : doc.entrySet()) {
                DataOrdenCompra dC = entry.getValue();
                Date F = dC.getEstado().getFecha();
                modelo.addRow(new Object[]{dC.getNumero(), sdf.format(F), dC.getTotal()});
            }
            TablaO.setModel(modelo);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

