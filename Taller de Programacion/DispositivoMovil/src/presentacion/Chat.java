    public Chat() throws Exception {
        initComponents();
        this.cs = CSesion.getInstance();
        cargarLista(listCqa);

        if (nProv.equals("")) {
            nProv = listCqa.getItemAt(0).toString();
            cargarBloque(txtAreaChat);
            lblChatCon.setText("Chat con " + nProv);
        } else {
            lblChatCon.setText("Chat con " + nProv);
        }

        PreguntarMens();
        cargarBloque(txtAreaChat);
    }

