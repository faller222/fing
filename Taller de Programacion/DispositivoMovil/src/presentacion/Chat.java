    private void PreguntarMens() {
        TimerTask timerTask = new TimerTask() {
            @Override
            public void run() {
                try {
                    if (cs.tengoMsj()) {
                        cargarLista(listCqa);
                        cargarBloque(txtAreaChat);
                    }
                } catch (Exception ex) {
                }
            }
        };
        if (timer != null) {
            timer = new Timer();
        }
        timer.scheduleAtFixedRate(timerTask, 0, 1000);
    }

    private void cargarLista(JComboBox Lista) throws Exception {
        List cqa = cs.conQuienHable();
        DefaultComboBoxModel LModel = new DefaultComboBoxModel();
        for (Object nom : cqa) {
            LModel.addElement((String) nom);
        }
        Lista.setModel(LModel);
    }

    private void cargarBloque(JTextArea Bloque) throws Exception {
        List cqa = cs.conversacionCon(nProv);
        String Text = "";
        for (Object msj : cqa) {
            Text = ((String) msj) + "\n" + Text;
        }
        Bloque.setText(Text);
    }

